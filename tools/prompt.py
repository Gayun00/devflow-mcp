def prompt(diff: str, slack_messages: list[str], jira_description: str) -> str:
    slack_part = "\n".join(f"- {m}" for m in slack_messages)
    return f"""
다음은 Git 코드 변경사항입니다:\n\n{diff}\n

이 변경사항을 바탕으로, 하나의 Pull Request에 들어갈 실무 수준의 요약을 작성해주세요.

다음 조건을 반드시 지켜주세요:

1. **변경의 핵심 목적 하나만 요약**해주세요 (불필요한 세부 내용 여러 개를 나열하지 마세요).
2. **전체 변경사항을 관통하는 문제와 해결 방식**을 간결히 설명하세요.
3. 각 섹션은 **불렛포인트 형태로 1~2줄 이내**로 작성해주세요.
4. 작성 언어는 반드시 **한국어**입니다.
5. 참고사항은 에러가 발생할 수 있는, 리뷰어가 반드시 주의해 확인해야하는 사항이 있는 경우에만 작성해주세요.

출력 형식은 다음과 같아야 합니다:

---

## 🧾 개요
- (이 PR의 전체적인 작업 목적을 간단히 서술)

## 🧩 변경 요약
- (어떤 문제가 있었는지 한 줄) {slack_part}에서 문제 현상이 적혀있다면 이 것을 참고해 요약
- (그 문제를 어떻게 해결했는지 한 줄) {jira_description}에서 필요한 작업내용이 적혀있다면 이것을 참고해 요약

## 📝 참고 사항
- (리뷰나 QA 시 주의할 점이나 추가 맥락 한 줄)
- (향후 개선 필요 사항이 있다면 한 줄)

🔧 이 설명은 자동으로 생성되었습니다.
"""
